def _circle(ax, x, y, r=0.18, color=C_BLUE):
    ax.add_patch(mpatches.Circle((x, y), r, facecolor=color,
                                 edgecolor=C_DARK, lw=1.0, zorder=3))


def _draw_mps(ax) -> None:
    ax.set_title("(a) Matrix Product State (Tensor Train)",
                 fontsize=10.5, color=C_DARK)
    n = 6
    xs = np.linspace(0.5, 5.5, n)
    y = 1.8
    for i, x in enumerate(xs):
        _circle(ax, x, y, color=C_BLUE)
        ax.text(x, y, f"$G_{i+1}$", color="white", fontsize=8.5,
                ha="center", va="center", fontweight="bold")
        # physical leg downward
        ax.plot([x, x], [y - 0.18, y - 0.7], color=C_DARK, lw=1.2)
        ax.text(x, y - 0.85, f"$i_{i+1}$", ha="center", fontsize=8.5,
                color=C_DARK)
    # bond legs
    for i in range(n - 1):
        ax.plot([xs[i] + 0.18, xs[i+1] - 0.18], [y, y], color=C_GRAY, lw=1.4)
    ax.text(3.0, 2.9,
            r"$\mathcal{X}(i_1,\ldots,i_N)=G_1(i_1)\,G_2(i_2)\cdots G_N(i_N)$",
            ha="center", fontsize=10, color=C_DARK)
    ax.text(3.0, 0.25, "open legs = physical indices, lines = bond dims",
            ha="center", fontsize=8.5, color=C_GRAY)
    ax.set_xlim(0, 6)
    ax.set_ylim(0, 3.4)
    ax.set_xticks([]); ax.set_yticks([])
    for s in ax.spines.values():
        s.set_visible(False)
    ax.grid(False)


def _draw_general_tn(ax) -> None:
    ax.set_title("(b) Tensor Network with loops",
                 fontsize=10.5, color=C_DARK)
    pts = {
        "A": (1.2, 2.6), "B": (2.6, 2.6), "C": (4.0, 2.6),
        "D": (1.9, 1.4), "E": (3.3, 1.4),
    }
    edges = [("A", "B"), ("B", "C"), ("A", "D"), ("D", "E"),
             ("E", "C"), ("D", "B"), ("E", "B")]
    for u, v in edges:
        x1, y1 = pts[u]; x2, y2 = pts[v]
        ax.plot([x1, x2], [y1, y2], color=C_GRAY, lw=1.4)
    # open legs
    open_legs = {"A": (-1, 0), "C": (1, 0), "D": (-0.4, -1), "E": (0.4, -1)}
    for k, (dx, dy) in open_legs.items():
        x, y = pts[k]
        nx_, ny_ = x + 0.7 * dx, y + 0.7 * dy
        ax.plot([x, nx_], [y, ny_], color=C_DARK, lw=1.2)
    for label, (x, y) in pts.items():
        _circle(ax, x, y, color=C_PURPLE)
        ax.text(x, y, label, color="white", fontsize=8.5,
                ha="center", va="center", fontweight="bold")
    ax.text(2.6, 0.25, "loops carry correlations across the lattice",
            ha="center", fontsize=8.5, color=C_GRAY)
    ax.set_xlim(0, 5.2)
    ax.set_ylim(0, 3.4)
    ax.set_xticks([]); ax.set_yticks([])
    for s in ax.spines.values():
        s.set_visible(False)
    ax.grid(False)


def _draw_mera(ax) -> None:
    ax.set_title("(c) MERA: layered, multi-scale",
                 fontsize=10.5, color=C_DARK)
    # Bottom layer (physical sites).
    n = 8
    xs = np.linspace(0.5, 5.5, n)
    y0 = 0.6
    for x in xs:
        _circle(ax, x, y0, r=0.13, color=C_GREEN)
        ax.plot([x, x], [y0 - 0.13, y0 - 0.4], color=C_DARK, lw=1.0)
    # Disentanglers (purple) layer.
    y1 = 1.4
    dx_pairs = [(xs[i], xs[i+1]) for i in (0, 2, 4, 6)]
    for x1, x2 in dx_pairs:
        cx = (x1 + x2) / 2
        ax.add_patch(mpatches.Rectangle((cx - 0.22, y1 - 0.12),
                                        0.44, 0.24,
                                        facecolor=C_PURPLE,
                                        edgecolor=C_DARK, lw=1.0))
        ax.plot([x1, cx - 0.10], [y0 + 0.13, y1 - 0.12], color=C_GRAY, lw=1.0)
        ax.plot([x2, cx + 0.10], [y0 + 0.13, y1 - 0.12], color=C_GRAY, lw=1.0)
    # Isometries (blue) higher layer: shift by one site.
    y2 = 2.2
    iso_pairs = [(xs[1], xs[2]), (xs[3], xs[4]), (xs[5], xs[6])]
    for x1, x2 in iso_pairs:
        cx = (x1 + x2) / 2
        ax.add_patch(mpatches.Circle((cx, y2), 0.18,
                                     facecolor=C_BLUE,
                                     edgecolor=C_DARK, lw=1.0))
        ax.plot([x1, cx - 0.13], [y1 + 0.12, y2 - 0.13], color=C_GRAY, lw=1.0)
        ax.plot([x2, cx + 0.13], [y1 + 0.12, y2 - 0.13], color=C_GRAY, lw=1.0)
    # Top layer.
    y3 = 3.0
    top_xs = [(iso_pairs[i][0] + iso_pairs[i][1]) / 2 for i in range(3)]
    for x in top_xs:
        ax.plot([x, x], [y2 + 0.18, y3], color=C_GRAY, lw=1.0)
    ax.text(3.0, y3 + 0.05, "coarse-grained scale",
            ha="center", fontsize=8.5, color=C_DARK)

    # Legend
    legend = [
        mpatches.Patch(color=C_GREEN, label="physical sites"),
        mpatches.Patch(color=C_PURPLE, label="disentanglers"),
        mpatches.Patch(color=C_BLUE, label="isometries"),
    ]
    ax.legend(handles=legend, loc="upper left", fontsize=8, framealpha=0.95)
    ax.set_xlim(-0.2, 6.2)
    ax.set_ylim(0, 3.6)
    ax.set_xticks([]); ax.set_yticks([])
    for s in ax.spines.values():
        s.set_visible(False)
    ax.grid(False)


