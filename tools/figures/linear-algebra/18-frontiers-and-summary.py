def _draw_bloch(ax) -> None:
    """Draw a Bloch sphere with several marked states on a 3D axis."""
    u = np.linspace(0, 2 * np.pi, 60)
    v = np.linspace(0, np.pi, 30)
    xs = np.outer(np.cos(u), np.sin(v))
    ys = np.outer(np.sin(u), np.sin(v))
    zs = np.outer(np.ones_like(u), np.cos(v))
    ax.plot_surface(xs, ys, zs, color=C_BLUE, alpha=0.10,
                    linewidth=0, antialiased=True)

    # Equator and meridians.
    theta = np.linspace(0, 2 * np.pi, 200)
    ax.plot(np.cos(theta), np.sin(theta), 0, color=C_GRAY, lw=0.8, alpha=0.7)
    ax.plot(np.cos(theta), 0 * theta, np.sin(theta), color=C_GRAY,
            lw=0.8, alpha=0.7)
    ax.plot(0 * theta, np.cos(theta), np.sin(theta), color=C_GRAY,
            lw=0.8, alpha=0.7)

    # Axes.
    for vec, color in [((1.2, 0, 0), C_GRAY),
                       ((0, 1.2, 0), C_GRAY),
                       ((0, 0, 1.2), C_GRAY)]:
        ax.plot([0, vec[0]], [0, vec[1]], [0, vec[2]],
                color=color, lw=1.0, alpha=0.7)

    # States: |0>, |1>, |+>, |->, plus arbitrary.
    states = [
        ((0, 0, 1),     "|0>",       C_BLUE),
        ((0, 0, -1),    "|1>",       C_PURPLE),
        ((1, 0, 0),     "|+>",       C_GREEN),
        ((-1, 0, 0),    "|->",       C_AMBER),
        ((0, 1, 0),     "|i>",       C_RED),
    ]
    for (x, y, z), label, c in states:
        ax.quiver(0, 0, 0, x, y, z, color=c, lw=2.0, arrow_length_ratio=0.10)
        ax.text(1.18 * x, 1.18 * y, 1.18 * z, label,
                color=c, fontsize=9, fontweight="bold")

    ax.set_xlim(-1.2, 1.2)
    ax.set_ylim(-1.2, 1.2)
    ax.set_zlim(-1.2, 1.2)
    ax.set_xticks([])
    ax.set_yticks([])
    ax.set_zticks([])
    ax.set_box_aspect([1, 1, 1])
    ax.set_title("(a) Bloch sphere: a qubit is a unit vector",
                 fontsize=10.5, color=C_DARK, pad=2)


def _draw_hadamard_action(ax) -> None:
    """Bar chart of |0> -> H -> (|0> + |1>) / sqrt(2)."""
    states = ["|0>", "|1>"]
    before = np.array([1.0, 0.0])
    after = np.array([1 / np.sqrt(2), 1 / np.sqrt(2)])

    x = np.arange(2)
    w = 0.36
    ax.bar(x - w / 2, before, width=w, color=C_BLUE, alpha=0.85,
           edgecolor="white", label="Before: |0>")
    ax.bar(x + w / 2, after, width=w, color=C_GREEN, alpha=0.85,
           edgecolor="white", label="After: H|0>")

    for xi, val in zip(x - w / 2, before):
        ax.text(xi, val + 0.02, f"{val:.2f}", ha="center",
                fontsize=8.5, color=C_DARK)
    for xi, val in zip(x + w / 2, after):
        ax.text(xi, val + 0.02, f"{val:.2f}", ha="center",
                fontsize=8.5, color=C_DARK)

    ax.set_xticks(x)
    ax.set_xticklabels(states, fontsize=10)
    ax.set_ylabel("Amplitude")
    ax.set_ylim(0, 1.15)
    ax.set_title("(b) Hadamard gate creates superposition",
                 fontsize=10.5, color=C_DARK)
    ax.legend(loc="upper right", fontsize=8.5)


def _draw_bell_circuit(ax) -> None:
    """Schematic Bell-state circuit and the resulting 4-vector amplitudes."""
    ax.set_xlim(0, 10)
    ax.set_ylim(0, 6)
    ax.set_xticks([])
    ax.set_yticks([])
    ax.set_title("(c) Bell state: |00> -> H tensor I -> CNOT", fontsize=10.5,
                 color=C_DARK)

    # Two qubit wires.
    ax.plot([0.5, 6.5], [4.5, 4.5], color=C_DARK, lw=1.4)
    ax.plot([0.5, 6.5], [3.0, 3.0], color=C_DARK, lw=1.4)
    ax.text(0.1, 4.5, "|0>", fontsize=10, va="center", color=C_DARK)
    ax.text(0.1, 3.0, "|0>", fontsize=10, va="center", color=C_DARK)

    # H gate.
    ax.add_patch(mpatches.Rectangle((1.6, 4.1), 0.8, 0.8,
                                    facecolor=C_BLUE, edgecolor=C_DARK))
    ax.text(2.0, 4.5, "H", color="white", ha="center", va="center",
            fontsize=11, fontweight="bold")

    # CNOT (control + target).
    ax.scatter([4.0], [4.5], color=C_DARK, s=60, zorder=5)
    ax.plot([4.0, 4.0], [3.0, 4.5], color=C_DARK, lw=1.2)
    circ = mpatches.Circle((4.0, 3.0), 0.30, fill=False,
                           edgecolor=C_DARK, lw=1.4)
    ax.add_patch(circ)
    ax.plot([3.7, 4.3], [3.0, 3.0], color=C_DARK, lw=1.2)
    ax.plot([4.0, 4.0], [2.7, 3.3], color=C_DARK, lw=1.2)

    ax.text(2.0, 5.4, "create\nsuperposition", ha="center",
            fontsize=8.5, color=C_BLUE)
    ax.text(4.0, 5.4, "entangle", ha="center",
            fontsize=8.5, color=C_PURPLE)

    # Amplitudes table on the right.
    labels = ["|00>", "|01>", "|10>", "|11>"]
    after_h = [1 / np.sqrt(2), 0, 1 / np.sqrt(2), 0]
    after_cnot = [1 / np.sqrt(2), 0, 0, 1 / np.sqrt(2)]
    base_x = 7.0
    col1_x = base_x + 0.40
    col2_x = base_x + 1.95
    ax.text(col1_x, 5.55, "after H$\\otimes$I", fontsize=8.5,
            color=C_BLUE, ha="left")
    ax.text(col2_x, 5.55, "after CNOT", fontsize=8.5,
            color=C_PURPLE, ha="left")
    for i, lab in enumerate(labels):
        y = 4.8 - 0.55 * i
        ax.text(base_x - 0.2, y, lab, fontsize=9, color=C_DARK, va="center")
        # bar1
        v1 = after_h[i]
        ax.add_patch(mpatches.Rectangle(
            (col1_x, y - 0.16), 0.55 * v1, 0.32,
            facecolor=C_BLUE, alpha=0.75, edgecolor="white"))
        ax.text(col1_x + 0.55 * v1 + 0.05, y, f"{v1:.2f}",
                fontsize=8, color=C_DARK, va="center")
        # bar2
        v2 = after_cnot[i]
        ax.add_patch(mpatches.Rectangle(
            (col2_x, y - 0.16), 0.55 * v2, 0.32,
            facecolor=C_PURPLE, alpha=0.75, edgecolor="white"))
        ax.text(col2_x + 0.55 * v2 + 0.05, y, f"{v2:.2f}",
                fontsize=8, color=C_DARK, va="center")

    ax.text(5.5, 1.0, r"$|\Phi^+\rangle = (|00\rangle + |11\rangle)/\sqrt{2}$",
            fontsize=11, color=C_DARK, ha="center")
    for spine in ax.spines.values():
        spine.set_visible(False)
    ax.grid(False)


