"""
Figure generation script for Linear Algebra Chapter 18: Frontiers and Summary.

This is the SERIES FINALE. The figures are designed to (a) recap the 18-chapter
journey as a single coherent picture, and (b) tease the frontier topics where
linear algebra continues to power new science.

Figures
-------
fig1_concept_map        Eighteen-chapter dependency graph drawn with networkx.
                        Nodes coloured by topical band (foundations, structure,
                        decomposition, computation, applications, frontiers);
                        edges show which earlier chapter each later chapter
                        builds on. The single image you can show after the
                        whole journey is finished.

fig2_quantum_computing  Quantum-computing primer in one panel:
                        (a) the Bloch sphere with several states marked;
                        (b) action of the Hadamard gate on |0>;
                        (c) a small Bell-state circuit and its 4-vector
                        amplitudes after H tensor I and after CNOT.

fig3_tensor_networks    Diagrammatic comparison of three tensor networks --
                        Matrix Product State (MPS / Tensor Train), a generic
                        Tensor Network with loops, and a MERA layered network.
                        Drawn with networkx + custom node placement; the
                        message is "open legs are physical indices, contracted
                        legs are bond dimensions".

fig4_lie_rotations      The matrix exponential as a one-parameter subgroup:
                        e^(theta K) traces a great-circle rotation on the
                        sphere. We show three orbits generated by Lx, Ly, Lz
                        (the so(3) basis) starting from a common point. The
                        infinitesimal generator (a tangent arrow) is drawn at
                        each starting point.

fig5_random_to_free     Two halves of one figure: left is the Marchenko-Pastur
                        bulk for a sample covariance (classical RMT); right
                        is the free additive convolution of two semicircles
                        producing a wider semicircle. The bridge between the
                        two: free probability gives the calculus that links
                        independent random matrices.

fig6_persistence        Topological data analysis preview: a noisy point cloud
                        sampled from an annulus on the left, and its
                        persistence diagram (H0 + H1) on the right. One
                        long-lived H1 point (the loop) sits well above the
                        diagonal; short-lived points hug it.

fig7_three_pillars      The book's recurring triangle: Geometry / Numerics /
                        Computation. Each vertex carries representative
                        artefacts from the series (Bloch sphere; condition
                        number; sparse-matrix kernel). The interior shows
                        SVD as the meeting point that all three perspectives
                        agree on.

Style
-----
matplotlib seaborn-v0_8-whitegrid, dpi=150.
Palette: #2563eb blue (primary), #7c3aed purple, #10b981 green, #f59e0b amber.

Output
------
PNGs are written to BOTH the EN and ZH article asset folders so the markdown
references stay consistent across languages.

Usage
-----
    python3 scripts/figures/linear-algebra/18-frontiers-and-summary.py
"""

