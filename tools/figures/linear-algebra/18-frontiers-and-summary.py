# Figure 6: Topological data analysis preview
# ---------------------------------------------------------------------------
def _vietoris_rips_h0_h1(points: np.ndarray):
    """Compute persistence diagrams for H0 and H1 using a simple
    Vietoris-Rips style approach that does not require external libraries.

    H0: classical single-linkage merge tree (births at radius 0, deaths when
        components merge).
    H1: detect the dominant 1-cycle by tracking the smallest cycle that
        forms as the radius grows. Returns a single approximate (birth, death)
        for the most persistent loop. This is a rough but adequate sketch
        for visualisation; the long-lived point will sit far above the
        diagonal for an annulus point cloud.
    """
    n = points.shape[0]
    # Pairwise distances.
    diff = points[:, None, :] - points[None, :, :]
    dist = np.sqrt((diff ** 2).sum(-1))

    # H0: Kruskal/union-find on edges in increasing order.
    parent = list(range(n))

    def find(a):
        while parent[a] != a:
            parent[a] = parent[parent[a]]
            a = parent[a]
        return a

    iu, ju = np.triu_indices(n, k=1)
    edges = sorted(zip(dist[iu, ju], iu, ju))
    h0 = []  # (birth=0, death=eps)
    n_components = n
    for d, i, j in edges:
        ri, rj = find(i), find(j)
        if ri != rj:
            parent[ri] = rj
            h0.append((0.0, d))
            n_components -= 1
        if n_components == 1:
            break
    # The very last component never dies; drop it from the diagram (or set
    # death = max edge as a finite proxy).
    h0 = h0[:-1] if h0 else h0

    # H1: detect when a 1-cycle first appears. For a Vietoris-Rips complex on
    # an annulus, the dominant 1-cycle's birth radius is roughly the largest
    # gap on the inner boundary; its death is roughly the inner radius.
    # We approximate: birth = first edge that closes a cycle when added to
    # the MST; death = radius at which the inner hole gets filled (estimated
    # as 2 * inner_radius).
    parent = list(range(n))
    cycle_births = []
    for d, i, j in edges:
        ri, rj = find(i), find(j)
        if ri == rj:
            cycle_births.append(d)
        else:
            parent[ri] = rj
    # Take the smallest cycle birth as the dominant H1 birth.
    if cycle_births:
        h1_birth = min(cycle_births)
        # Death heuristic: when balls of radius r/2 cover the hole. For a
        # unit-ish annulus we use the inner radius times sqrt(3).
        center = points.mean(axis=0)
        radii = np.sqrt(((points - center) ** 2).sum(-1))
        inner_r = radii.min()
        h1_death = max(h1_birth + 0.05, np.sqrt(3) * inner_r)
        h1 = [(h1_birth, h1_death)]
    else:
        h1 = []

    return np.array(h0), np.array(h1)


def fig6_persistence() -> None:
    # Sample a noisy annulus.
    n = 80
    angles = RNG.uniform(0, 2 * np.pi, n)
