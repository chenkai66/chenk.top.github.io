def _so3_basis():
    Lx = np.array([[0, 0, 0], [0, 0, -1], [0, 1, 0]], dtype=float)
    Ly = np.array([[0, 0, 1], [0, 0, 0], [-1, 0, 0]], dtype=float)
    Lz = np.array([[0, -1, 0], [1, 0, 0], [0, 0, 0]], dtype=float)
    return Lx, Ly, Lz


def _expm_skew(K: np.ndarray, theta: float) -> np.ndarray:
    """Closed form for the exponential of a 3x3 skew matrix (Rodrigues)."""
    # Identify axis from skew matrix.
    w = np.array([K[2, 1], K[0, 2], K[1, 0]])
    n = np.linalg.norm(w)
    if n < 1e-12:
        return np.eye(3)
    k = w / n
    K_hat = np.array([[0, -k[2], k[1]],
                      [k[2], 0, -k[0]],
                      [-k[1], k[0], 0]])
    return (np.eye(3)
            + np.sin(theta * n) * K_hat
            + (1 - np.cos(theta * n)) * (K_hat @ K_hat))


