    A = np.array([5.0, 8.4])    # top: Geometry
    B = np.array([0.8, 1.2])    # bottom-left: Numerics
    C = np.array([9.2, 1.2])    # bottom-right: Computation

    tri = mpatches.Polygon([A, B, C], closed=True,
                           facecolor=C_LIGHT, alpha=0.30,
                           edgecolor=C_DARK, lw=1.4)
    ax.add_patch(tri)

    # Centroid: SVD as the meeting point.
    centroid = (A + B + C) / 3.0
    ax.add_patch(mpatches.Circle(centroid, 0.55,
                                 facecolor=C_GREEN, edgecolor=C_DARK, lw=1.4))
    ax.text(centroid[0], centroid[1], "SVD",
            ha="center", va="center", fontsize=13,
            color="white", fontweight="bold")
    ax.text(centroid[0], centroid[1] - 0.95,
            "the one decomposition\nthe three pillars agree on",
            ha="center", fontsize=8.5, color=C_DARK)

    # Vertex labels and example artefacts.
    pillars = [
        (A, C_BLUE, "Geometry",
         "vectors, projections,\neigenframes, rotations,\nBloch sphere"),
        (B, C_PURPLE, "Numerics",
         "norms, conditioning,\nstable algorithms,\nfloating point"),
        (C, C_AMBER, "Computation",
         "sparse kernels,\nrandomised methods,\nGPU tensor cores"),
    ]
    for (pos, color, name, body) in pillars:
        ax.add_patch(mpatches.Circle(pos, 0.85,
                                     facecolor=color, edgecolor=C_DARK,
                                     lw=1.6, alpha=0.95))
        ax.text(pos[0], pos[1], name,
                ha="center", va="center", fontsize=12,
                color="white", fontweight="bold")
        # Body text outside the circle, in a corner of the figure for the
        # apex so it does not collide with the title.
        if pos is A:
            ax.text(pos[0] + 1.1, pos[1] + 0.0, body,
                    ha="left", va="center", fontsize=9.5, color=C_DARK)
        elif pos is B:
            ax.text(pos[0] - 0.8, pos[1] - 1.25, body,
                    ha="center", va="top", fontsize=9.5, color=C_DARK)
        else:
            ax.text(pos[0] + 0.8, pos[1] - 1.25, body,
                    ha="center", va="top", fontsize=9.5, color=C_DARK)

    # Edge labels (themes that bridge two pillars).
    def midpt(p, q): return (p + q) / 2.0
    edges = [
        (A, B, "spectral theory\nlinks shape to stability"),
        (A, C, "sketching: keep\ngeometry at scale"),
        (B, C, "low-precision\narithmetic"),
    ]
    for p, q, label in edges:
        m = midpt(p, q)
        # nudge outward
        n_vec = np.array([-(q - p)[1], (q - p)[0]])
        n_vec = n_vec / (np.linalg.norm(n_vec) + 1e-12)
        # choose direction away from centroid
        if np.dot(m - centroid, n_vec) < 0:
            n_vec = -n_vec
        m_out = m + 0.7 * n_vec
        ax.text(m_out[0], m_out[1], label,
                ha="center", va="center", fontsize=8.5,
                color=C_DARK, style="italic",
                bbox=dict(facecolor="white", edgecolor=C_GRAY,
                          boxstyle="round,pad=0.25", alpha=0.9))

    ax.set_xlim(-1.5, 11.5)
    ax.set_ylim(-0.6, 10.4)
    ax.set_xticks([]); ax.set_yticks([])
    ax.set_aspect("equal")
    ax.set_title(
        "The recurring triangle of the series:\ngeometry, numerics, computation",
        fontsize=13, color=C_DARK,
    )
    for s in ax.spines.values():
        s.set_visible(False)
    ax.grid(False)

    save(fig, "fig7_three_pillars")


